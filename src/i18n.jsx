export function normalizeLanguage(value) {
  const normalized = String(value || '').trim().toLowerCase();
  if (normalized === 'en' || normalized.startsWith('en-')) return 'en';
  if (normalized === 'zh' || normalized.startsWith('zh-')) return 'zh';
  return null;
}

function browserLanguage() {
  if (typeof navigator === 'undefined') return 'zh';
  return normalizeLanguage(navigator.languages?.[0] || navigator.language) || 'zh';
}

function storedLanguage() {
  try { return normalizeLanguage(window.localStorage.getItem(LANGUAGE_STORAGE_KEY)); } catch { return null; }
}

function interpolate(value, variables) {
  return String(value).replace(/\{([a-zA-Z0-9_]+)\}/g, (_, key) => (
    variables?.[key] === undefined || variables?.[key] === null ? '' : String(variables[key])
  ));
}

const I18nContext = createContext(null);

export function I18nProvider({ children }) {
  const storedAtStart = useRef(storedLanguage());
  const userSelected = useRef(Boolean(storedAtStart.current));
  const [language, setLanguageState] = useState(storedAtStart.current || browserLanguage());

  const setLanguage = useCallback((nextLanguage) => {
    const normalized = normalizeLanguage(nextLanguage) || 'zh';
    userSelected.current = true;
    setLanguageState(normalized);
    try { window.localStorage.setItem(LANGUAGE_STORAGE_KEY, normalized); } catch { /* Keep the current session. */ }
  }, []);

  const applyProjectDefault = useCallback((nextLanguage) => {
    if (userSelected.current) return;
    const normalized = normalizeLanguage(nextLanguage);
    if (normalized) setLanguageState(normalized);
  }, []);

  useEffect(() => {
    document.documentElement.lang = language === 'en' ? 'en' : 'zh-CN';
  }, [language]);

  const t = useCallback((key, variables = {}, fallback = key) => {
    const value = messages[language]?.[key] ?? messages.zh[key] ?? fallback;
    return interpolate(value, variables);
  }, [language]);

  const formatDateTime = useCallback((value) => {
    if (!value) return t('common.notRecorded');
    const date = new Date(value);
    return Number.isNaN(date.getTime())
      ? String(value)
      : date.toLocaleString(language === 'en' ? 'en-US' : 'zh-CN', { hour12: false });
  }, [language, t]);

  const translateError = useCallback((error) => {
    if (!error) return t('errors.generic', { code: 'UNKNOWN' });
    if (error.name === 'TypeError' && !error.code) return t('errors.network');
    if (errorKeys.has(error.code)) return t(`errors.${error.code}`);
    if (language === 'zh' && error.message) return error.message;
    return t('errors.generic', { code: error.code || error.status || 'UNKNOWN' });
  }, [language, t]);

  const value = useMemo(() => ({
    language,
    setLanguage,
    applyProjectDefault,
    t,
    formatDateTime,
    translateError,
  }), [applyProjectDefault, formatDateTime, language, setLanguage, t, translateError]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error('useI18n must be used inside I18nProvider');
  return context;
}

export function LanguageSwitch() {
  const { language, setLanguage, t } = useI18n();
  const nextLanguage = language === 'zh' ? 'en' : 'zh';
  return (
    <nav className="language-switch" aria-label={t('language.switch')}>
      <button
        type="button"
        aria-label={t(nextLanguage === 'en' ? 'language.switchToEnglish' : 'language.switchToChinese')}
        onClick={() => setLanguage(nextLanguage)}
      >
        {t(nextLanguage === 'en' ? 'language.english' : 'language.chinese')}
      </button>
    </nav>
  );
}
